package com.project.pojo;

public class Matches {
	int countSingle;
	int countDouble;

	public Matches(int countSingle, int countDouble) {
		this.countSingle = countSingle;
		this.countDouble = countDouble;
	}

	public int getCountSingle() {
		return countSingle;
	}

	public int getCountDouble() {
		return countDouble;
	}

}
